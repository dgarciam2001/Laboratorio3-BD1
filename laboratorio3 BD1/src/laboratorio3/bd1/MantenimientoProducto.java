    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/cafeteria", "root", "compromete");
            PreparedStatement pst = cn.prepareStatement("select * from producto where IdProducto = ?");
            pst.setString(1, txtBuscar.getText().trim());

            ResultSet rs = pst.executeQuery();

            if(rs.next()){
                txtIdProduc.setText(rs.getString("IdProducto"));
                txtNombre.setText(rs.getString("NombreProducto"));
                txtMarca.setText(rs.getString("Marca"));
                txtLinea.setText(rs.getString("Linea"));
                txtCosto.setText(rs.getString("costo"));
                txtPrecio.setText(rs.getString("PrecioProducto"));
                txtFacCom.setText(rs.getString("FacultadCompra"));
                txtFacVen.setText(rs.getString("FacultadVenta"));
                txtFecha.setText(rs.getString("Fecha_Caducidad"));
            } else {
                JOptionPane.showMessageDialog(null, "Producto no registrado.");
                txtBuscar.setText("");
            }

        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "No se pudo acceder a la Base de Datos.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


